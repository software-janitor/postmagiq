def seed_voice_profiles():
    """Seed voice profile presets into the database."""
    print("Initializing database...")
    init_db()

    # Build profile list
    profiles_to_seed = []

    # 1. Parse Matthew Garcia profile from file
    voice_profile_path = Path(__file__).parent.parent / "prompts" / "voice_profiles" / "matthew-garcia.md"
    if voice_profile_path.exists():
        print(f"Reading Matthew Garcia profile from: {voice_profile_path}")
        matthew_profile = parse_matthew_garcia_profile(voice_profile_path)
        profiles_to_seed.append(matthew_profile)
    else:
        print(f"WARNING: Matthew Garcia profile not found at {voice_profile_path}")

    # 2. Add other preset profiles
    profiles_to_seed.extend(PRESET_PROFILES)

    with Session(engine) as session:
        created_count = 0
        updated_count = 0

        for profile_data in profiles_to_seed:
            # Check if profile exists by slug
            existing = session.exec(
                select(VoiceProfile).where(
                    VoiceProfile.slug == profile_data["slug"],
                    VoiceProfile.workspace_id == None,
                )
            ).first()

            if existing:
                # Update existing profile
                print(f"  Updating preset: {profile_data['name']} (slug: {profile_data['slug']})")
                for key, value in profile_data.items():
                    setattr(existing, key, value)
                existing.is_preset = True
                session.add(existing)
                updated_count += 1
            else:
                # Create new profile
                print(f"  Creating preset: {profile_data['name']} (slug: {profile_data['slug']})")
                profile = VoiceProfile(
                    id=uuid4(),
                    workspace_id=None,  # System preset
                    is_preset=True,
                    **profile_data,
                )
                session.add(profile)
                created_count += 1

        session.commit()

        print("\n" + "=" * 50)
        print("Voice profile presets seeded successfully!")
        print("=" * 50)
        print(f"\nCreated: {created_count}")
        print(f"Updated: {updated_count}")
        print(f"Total presets: {len(profiles_to_seed)}")


if __name__ == "__main__":
    seed_voice_profiles()
