def parse_matthew_garcia_profile(file_path: Path) -> dict:
    """Parse the Matthew Garcia voice profile from markdown file."""
    content = file_path.read_text()

    # Extract sections
    sections = {}
    current_section = None
    current_content = []

    for line in content.split('\n'):
        if line.startswith('## '):
            if current_section:
                sections[current_section] = '\n'.join(current_content).strip()
            current_section = line[3:].strip()
            current_content = []
        elif current_section:
            current_content.append(line)

    if current_section:
        sections[current_section] = '\n'.join(current_content).strip()

    # Extract signature thesis from intro
    thesis_match = re.search(r'\*\*Signature Thesis:\*\*\s*(.+)', content)
    signature_thesis = thesis_match.group(1).strip() if thesis_match else None

    # Build tone description from "Your Voice" section
    tone_description = sections.get('Your Voice', '')

    # Extract signature phrases
    signature_phrases = sections.get('Signature Phrases (use sparingly)', '')

    # Extract word choices
    word_choices = sections.get('Word Choices', '')

    # Extract example excerpts
    example_excerpts = sections.get('Example Excerpts', '')

    # Extract avoid patterns
    avoid_patterns = sections.get('Avoid', '')

    return {
        "name": "Matthew Garcia",
        "slug": "matthew-garcia",
        "description": f"Distinguished/Partner Engineer focused on AI platform development. {signature_thesis}",
        "tone_description": tone_description,
        "signature_phrases": signature_phrases,
        "word_choices": word_choices,
        "example_excerpts": example_excerpts,
        "avoid_patterns": avoid_patterns,
    }


